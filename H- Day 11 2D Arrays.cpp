//====================================================//
int main()
{
    int sum , max = -(9*7);
    vector<vector<int>> arr(6, vector<int>(6));
    for (int i = 0; i < 6; i++)
    {
        for (int j = 0; j < 6; j++)
        {
            cin >> arr[i][j];
        }
    } 
    // The number of hourGlasses in an array = (r-2)*(c-2)                     condition :  ( r >=3 && c >= 3)
    for (int i = 0; i < 4; i++) // 4 is the (number of rows - 2) 
    {
        for (int j = 0; j < 4; j++) // 4 is the (number of columns - 2)
        {
            sum = arr[i][j] + arr[i][j + 1] + arr[i][j + 2] + arr[i + 1][j + 1] + arr[i + 2][j] + arr[i + 2][j + 1] + arr[i + 2][j + 2];
        if (sum > max) max = sum;
        }
    }
    cout << max;
    return 0;
}
